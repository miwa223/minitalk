#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*ans;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == (char)c)
		{
			ans = (char *)(s + i);
			return (ans);
		}
		i--;
	}
	return (NULL);
}
